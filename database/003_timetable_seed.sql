upsert_classes AS (
    INSERT INTO classes (designation, short_name, class_type)
    SELECT name, code, class_type::class_type_enum FROM subject_input
    ON CONFLICT (designation) DO UPDATE SET 
        class_type = EXCLUDED.class_type,
        short_name = EXCLUDED.short_name
    RETURNING id, designation
)
